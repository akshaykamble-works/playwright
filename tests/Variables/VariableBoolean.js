let a=10;
let b=20;
let c=a>b;
console.log("value of a "+a+ " and value of b "+b+ " "+c); // Output: false

let d=a<b;
console.log("value of a "+a+ " and value of b "+b+ " "+d); // Output: true

let e=a==b;
console.log("value of a "+a+ " and value of b "+b+ ""+e); // Output: false

/*
Key Points:
- Only two values: true or false
- Result of comparisons (5 > 3 → true)
- Falsy values: false, 0, "", null, undefined, NaN
- Truthy values: Everything else
*/