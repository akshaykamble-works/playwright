// Relational Operators
const a = 10;
const b = 20;

console.log("Relational Operators:");
console.log("a == b: " + (a == b));
console.log("a != b: " + (a != b));
console.log("a > b: " + (a > b));
console.log("a < b: " + (a < b));
console.log("a >= b: " + (a >= b));
console.log("a <= b: " + (a <= b));
console.log("a === b: " + (a === b));
console.log("a !== b: " + (a !== b));

// Logical Operators
const x = true;
const y = false;

console.log("Logical Operators:");
console.log("x && y: " + (x && y));
console.log("x || y: " + (x || y));
console.log("!x: " + (!x));

