// Number data type
let age = 30;
console.log(age); // Output: 30

// String data type
let name = "John";
console.log(name); // Output: John

// Boolean data type
let isStudent = true;
console.log(isStudent); // Output: true

// Undefined data type
let address;
console.log(address); // Output: undefined

// Null data type
let phoneNumber = null;
console.log(phoneNumber); // Output: null
