// Do while Loop
// Definition: A control flow statement that executes a block of code at least once, and then repeatedly executes it as long as a specified condition is true.
// Syntax:
// do {
//   // code block to be executed
// } while (condition);

// Example:
let i = 0;
do {
  console.log(i);
  i++;
} while (i < 5);
